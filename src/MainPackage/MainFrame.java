    private void combox_fill(JComboBox comboxname, ResultSet st) {
        int currentpos = comboxname.getSelectedIndex();
        String filter = (String) comboxname.getEditor().getItem();
        try {
            comboxname.removeAllItems();
            while (st.next()) {
                if (filter == null || filter.isEmpty()) {
                    comboxname.addItem(st.getString(1));
                } else {
                    if (st.getString(1).contains(filter)) {
                        comboxname.addItem(st.getString(1));
                    }
                }
            }
        } catch (SQLException ex) {
        }
        comboxname.setSelectedIndex(-1);
    }

    private int Comboxname(JComboBox combox, String name) {
        for (int i = 0; i < combox.getItemCount(); i++) {
            if (combox.getItemAt(i).toString().equalsIgnoreCase(name)) {
                return i;
            }
        }
        return -1;
    }

