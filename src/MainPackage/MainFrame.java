    private void jButton_m5zn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_m5zn_searchActionPerformed
        // TODO add your handling code here:
        if (!(jComboBox_M5zn.getSelectedIndex() < 0)) {
            try {
                ResultSet st = opj.dataRead("pro_type", "product", "pro_name=N'" + jComboBox_M5zn.getSelectedItem().toString() + "'");
                String colm = "";
                double totalwzn = 0.0;
                int total3dd = 0;
                if (st.next()) {
                    colm = st.getString(1).equals("غزل") ? "wzn,_3add" : "mtr,_3add";
                }
                st = opj.dataRead(colm, "yomya", "_3mlya = N'وارد' and pro_id = (select pro_id from product where pro_name = N'" + jComboBox_M5zn.getSelectedItem().toString() + "')");
                while (st.next()) {
                    totalwzn += st.getDouble(1);
                    total3dd += st.getInt(2);
                }

                st = opj.dataRead(colm, "yomya", "_3mlya = N'صادر' and pro_id = (select pro_id from product where pro_name = N'" + jComboBox_M5zn.getSelectedItem().toString() + "')");
                while (st.next()) {
                    totalwzn -= st.getDouble(1);
                    total3dd -= st.getInt(2);
                }
                total3dd = total3dd < 0 ? 0 : total3dd;
                totalwzn = totalwzn < 0.0 ? 0.0 : Math.round(totalwzn);
                jTextField_M5zn_Count.setText(total3dd + "");
                if (colm.contains("wzn")) {
                    jTextField_M5zn_Wight.setText(totalwzn + "");
                    jTextField_M5zn_Length.setText("");
                } else {
                    jTextField_M5zn_Wight.setText("");
                    jTextField_M5zn_Length.setText(totalwzn + "");
                }

            } catch (SQLException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else
            JOptionPane.showMessageDialog(null, "choose a fkin product", "خطأ", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_jButton_m5zn_searchActionPerformed

