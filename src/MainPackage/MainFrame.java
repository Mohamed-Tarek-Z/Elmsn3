    private void textbox_length_limiter(KeyEvent event, JTextField textboxname, int length) {
        if (textboxname.getText().length() > length - 1) {
            event.consume();
        }
    }

    private void textbox_number(KeyEvent event) {
        char input = event.getKeyChar();
        if ((input < '0' || input > '9')) {
            event.consume();
        }
    }

    private void textbox_text(KeyEvent event) {
        char input = event.getKeyChar();
        if (!(input < '0' || input > '9')) {
            event.consume();
        }
    }

    private void textbox_number_money(KeyEvent event, JTextField textboxname) {
        char input = event.getKeyChar();
        if ((input < '0' || input > '9')) {
            if (input == '.') {
                if (textboxname.getText().contains(".")) {
                    event.consume();
                }
            } else {
                event.consume();
            }
        } else {
            if (textboxname.getText().contains(".")) {
                if (textboxname.getText().indexOf(".") == textboxname.getText().length() - 3) {
                    event.consume();
                }
            }
        }
    }

