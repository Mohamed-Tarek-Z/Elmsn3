    private void jTable_monyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_monyMouseClicked
        // TODO add your handling code here:
        if (jTable_mony.getSelectedRow() > -1) {
            int i = jTable_mony.getSelectedRow();
            TableModel model = jTable_mony.getModel();
            try {
                jDateChooser_mony.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(i, 1).toString()));
            } catch (ParseException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            jComboBox_mony_Client.setSelectedIndex(Comboxname(jComboBox_mony_Client, model.getValueAt(i, 2).toString()));

            if (model.getValueAt(i, 3).toString().equalsIgnoreCase("وارد")) {
                jRadio_mony_Wared.setSelected(true);
            } else {
                jRadio_mony_Sader.setSelected(true);
            }
            if (model.getValueAt(i, 4).toString().equalsIgnoreCase("نقدية")) {
                jRadio_mony_cash.setSelected(true);
            } else {
                jRadio_mony_shek.setSelected(true);
                try {
                    jDateChooser_mony_chekdate.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(i, 6).toString()));
                    //jTextField_nqdya_nochek.setText(model.getValueAt(i, 7).toString());
                } catch (ParseException ex) {
                    Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            jTextField_mony_Mony.setText(model.getValueAt(i, 5).toString());
            jTextField_mony_notes.setText(model.getValueAt(i, 8) == null ? " " : model.getValueAt(i, 8).toString());
            nqdya_Table_SelectedID = Integer.parseInt(model.getValueAt(i, 0).toString());
            ResultSet st = opj.dataRead("mortg3at", "yomyanqdya", "id=" + nqdya_Table_SelectedID);
            try {
                if (st.next()) {
                    if (st.getString(1).equals("0")) {
                        jCheckBox_mony_Mortg3.setSelected(false);
                    } else if (st.getString(1).equals("1")) {
                        jCheckBox_mony_Mortg3.setSelected(true);
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jTable_monyMouseClicked

